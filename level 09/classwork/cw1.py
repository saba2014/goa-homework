# დაწერეთ while loop-ით კოდი რომელიც დაითვლის 1 დან 10 მდე

i = 1
while i <= 10:
    print(i)
    i += 1  


# დაწერეთ while loop-თ კოდი რომელიც დაითვლის 10-1 მდე

i = 10
while i >= 1:
    print(i)
    i -= 1 

# დაწერეთ უსასრულო while loop

while True:
    print("ეს ციკლი უსასრულოა")

