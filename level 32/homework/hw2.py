# სტრიქონების გაერთიანება შექმენით სია სახელწოდებით words, რომელიც შეიცავს შემდეგ სტრიქონებს: "apple", "banana", "cherry". გამოიყენეთ join() ფუნქცია, რომ გაერთიანოთ ეს სიტყვები ერთ სტრიქონში, გამოყოფილი კომითა და ცარიელი ადგილით ", ". შეინახეთ შედეგი ცვლადში სახელწოდებით fruit_string და დაბეჭდეთ იგი.


words = ["apple", "banana", "cherry"]
fruit_string = ", ".join(words)
print(fruit_string)
